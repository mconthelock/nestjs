CREATE TABLE DS_ITEM (
    ITEM_ID INT,
    ITEM_NO VARCHAR(255),
    ITEM_NAME VARCHAR(255),
    ITEM_DWG VARCHAR(255),
    ITEM_VARIABLE VARCHAR(255),
    ITEM_TYPE INT,
    ITEM_CLASS VARCHAR(255),
    ITEM_UNIT VARCHAR(255),
    ITEM_SUPPLIER VARCHAR(255),
    CATEGORY INT,
    ITEM_REMARK TEXT,
    ITEM_CUS_PUR VARCHAR(255),
    ITEM_AMEC_PUR VARCHAR(255),
    ITEM_MODEL VARCHAR(255),
    ITEM_STATUS INT,
    ITEM_THUMB VARCHAR(255),
    CREATE_AT TIMESTAMP(6),
    CREATE_BY VARCHAR(255),
    UPDATE_AT TIMESTAMP(6),
    UPDATE_BY VARCHAR(255)
);
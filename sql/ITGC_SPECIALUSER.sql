CREATE TABLE ITGC_SPECIALUSER (
	SERVER_NAME VARCHAR(50) NOT NULL,
	USER_DOMAIN VARCHAR(255) NOT NULL,
	USER_LOGIN VARCHAR(50) NOT NULL,
	USER_OWNER VARCHAR(255) NULL,
	DESCRIPT VARCHAR(255) NULL,
	AUTH_CLASS VARCHAR(255) NULL,
	CATEGORY VARCHAR(5) NOT NULL,
	AUTH_OGANIZE VARCHAR(255) NULL,
	USER_TYPE1 VARCHAR(255) NULL,
	USER_TYPE2 VARCHAR(255) NULL,
	SERVER_TITLE VARCHAR(100) NOT NULL,
	USER_STATUS INT NULL,
	USER_EMAIL VARCHAR(255) NULL,
	EMPNO CHAR(10) NULL,
	ROLE VARCHAR(100) NULL,
	START_DATE DATE NULL,
	ACTIVE_STATUS INT NULL,
	GROUP_NAME VARCHAR(100) NULL,
	CONSTRAINT PK_ITGC_SPECIALUSER PRIMARY KEY (SERVER_NAME,CATEGORY,USER_LOGIN)

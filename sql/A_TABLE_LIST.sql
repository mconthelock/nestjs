CREATE TABLE A_TABLE_LIST (
	TB_LIBRARY VARCHAR(50),
	TB_NAME VARCHAR(50),
	TB_SOURCE VARCHAR(20),
	TB_EXTRACT VARCHAR(20),
	TB_STATUS TINYINT(1),
	ID INT AUTO_INCREMENT,
	CONDITIONS VARCHAR(512),
	PIC VARCHAR(50),
	REMARK VARCHAR(1024),
	KEY_COLUMN VARCHAR(300),
	SOURCE_REC INT,
	TARGET_REC INT,
	TB_COLS VARCHAR(128),
	SOURCE_CHECK TIMESTAMP(6),
	TARGET_CHECK TIMESTAMP(6),
	LAST_SYNC TIMESTAMP(6),
	DEFAULTJRN VARCHAR(15),
	TB_DATABASE VARCHAR(20),
	SKIPCHECK CHAR(1) DEFAULT '0',
	PRIMARY KEY (ID)
);